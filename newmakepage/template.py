    html+="中评社香港10月12日电／澳门近年来赌场业兴隆，澳政府称赌场带动当地经济增长。但政治分析家说，赌场的兴旺只让少数人致富，穷人和中产阶级的生活水准却在降低。人们普遍不满于日益严重的贫富差距及澳门政府一连串的贿赂丑闻，不久前，10月1日爆发了上千人的抗议，这是继今年5月1日上万人大游行后的再次民怨沸腾。 <BR><BR>　　“赌博经济”加剧贫富差距 <BR><BR>　　纽约时报9日报导，澳门“赌博经济”下的大型赌场连立，但讽刺的是：豪华赌场几英哩外的街边，是一排排拥挤的骯脏公寓。在葡萄牙殖民统治时期，这一区域居住着澳门最贫穷的一些居民。 <BR><BR>　　尽管近年来“赌博经济”增长，但这一贫穷地区居民说这里几乎没有变化，他们没有从纸醉金迷的赌场得到什么好处。 <BR><BR>　　一位名叫关进南的居民最近被最新最大的赌场——威尼斯人渡假村中的葡萄牙餐馆雇用为厨师。不过23岁的关先生很快发现，赌场的工作不能使他摆脱贫穷，关每个月赚7千澳元，按当地标准收入应属还可以，但不是很好。 <BR><BR>　　像许多其他的澳门人，他抱怨说，大量涌入的大陆劳动工人使薪水降低，而生活费用，特别对工人阶层来说，在快速上涨。他说：“大陆人在家乡一个月可能收入3千澳元”，“可是来这里能赚6千元。对他们可能收益很高，但对我们来说就太低了。” <BR><BR>　　澳门立法委员会委员高天赐说：“政府告诉我们，赌场的兴旺会给澳门带来财富”。但这“不真实，因为只有少数人致富，而没有改变穷人的贫苦生活。” <BR><BR>　　公众不满政府&nbsp;抗议频频 <BR><BR>　　报导说，澳门传统上政治环境较为平静，但近几个月来公众开始对政府表示不满。10月1日爆发了几千人的抗议。他们抱怨政府严格惩罚非法停车、官员腐败和非法劳工问题。 <BR><BR>　　这次抗议类似于今年5月爆发的强烈抗议。5月1日澳门上万人参加自1999年回归以来最大的一次抗议，他们不满于政府越来越腐败，并允许外国工作者进澳门工作，游行队伍呼喊着要求澳门首席执行官何厚铧辞职。 <BR><BR>　　收入不均愈发严重 <BR><BR>　　金融时报报导指出，澳门最近的反政府情绪有一个共同主题，立法委员和政治分析家说：穷人和中产阶级的生活水准正在降低，而首席执行官何厚铧的政府置若罔闻。欧盟今年8月有关澳门情况的年终报告说，赌博繁荣导致“收入不均愈发严重。”\" <BR><BR>　　随着大批外籍劳工进入澳门，主要来自中国大陆和东南亚，致使劳动力市场愈发竞争激烈，目前大约已有7万外籍劳工，占总劳力的四分之一，加之薪水跟不上物价上涨。房地产价格和住房租金估计上升了200％到300％。 <BR><BR>　　这些问题与官方的经济资料似乎不相吻合，官方描绘澳门获得巨大的经济成功，去年的经济增长率为16.6％，是世界上发展率最快的一个。今年8月的失业率只有3.1％。 <BR><BR>　　该城市23个赌场2006年收入达68.7亿美元，超过美国的拉斯维加斯成为世界上最大的赌博市场。根据政府报告，澳门居民2006年平均收入达到28,436美元，可与香港相比。 <BR><BR>　　但对于那些年龄较大的工作者和不在赌场或文职机关工作的人而言，机会和收入都越来越少。许多人的薪水不到赌场发牌人员的四分之一。 <BR><BR>　　政治分析家说，人们普遍对日益严重的收入不均失望，又进一步被政府一连串的贿赂丑闻所激怒。所有这些鼓励了澳门的民主人士，促使更多的人加入到要求民主的行列。 <BR><BR>　　报导说，如同香港的基本法，1987年的中葡联合声明和澳门基本法不把投票权作为政治改革的最终目标。 <BR><BR>　　澳门大学的一位政治学讲师于艾乐指出：“澳门社会正在发展并开始动员，越来越多的人参与政治，最终人们将会向政府挑战。”"
